CREATE TABLE FOODTRIANGLEDAY
(
ID INTEGER NOT NULL,
DAY VARCHAR(255) NOT NULL,
FRUITSAMOUNT INTEGER NOT NULL,
VEGETABLESAMOUNT INTEGER NOT NULL,
GRAINSAMOUNT INTEGER NOT NULL,
PROTEINAMOUNT INTEGER NOT NULL,
DAIRYAMOUNT INTEGER NOT NULL,
OILSAMOUNT INTEGER NOT NULL,
USERID INTEGER NOT NULL,
PRIMARY KEY (ID),
FOREIGN KEY (USERID) REFERENCES FOODTRIANGLEUSER(ID)
	ON DELETE CASCADE
	ON UPDATE NO ACTION
);
