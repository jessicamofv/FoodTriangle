CREATE TABLE FOODTRIANGLEUSER
(
ID INTEGER NOT NULL,
FIRSTNAME VARCHAR(255) NOT NULL,
LASTNAME VARCHAR(255) NOT NULL,
SEX CHAR(6) NOT NULL,
